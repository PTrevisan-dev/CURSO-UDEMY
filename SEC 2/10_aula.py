# tupla 

filmlist = ('Kung fu panda', 'Interstellar', 'Intocaveis', 
            'star vs forças do mal')

print(type(filmlist))

# 1- buscar os dois primeiros itens da tupla 
print(filmlist[:2])

# 2- buscar o ultimo iten da tupla 
print(filmlist[-1])

# 3- buscar filmes ate uma certa posição 
print(filmlist[:3])

# 4- buscar filmes de uma posição em diante
print(filmlist[2:])

# 5- recuperar item 
print(filmlist.index('star vs forças do mal'))