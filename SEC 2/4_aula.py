# operadores

num1 = int(input('digite o primeiro numero\n'))
num2 = int(input('digite o segundo numero\n'))

#aritimeticos 

sum = num1 + num2
sub = num1 - num2
div = num1 / num2 
mult = num1 * num2
mod = num1 % num2 
exp = num1 ** num2

print(sum)
print(sub)
print(div)
print(mult)
print(mod)
print(exp)

# comparação 

bigger = num1 > num2
smaller = num1 < num2
equal = num1 == num2 
different = num1 !=  num2
bigger_equal = num1 >= num2 
smaller_equal = num1 <= num2

print(bigger)
print(smaller)
print(equal)
print(different)
print(bigger_equal)
print(smaller_equal)

